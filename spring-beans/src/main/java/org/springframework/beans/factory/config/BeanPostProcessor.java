package org.springframework.beans.factory.config;

import org.springframework.beans.BeansException;
import org.springframework.lang.Nullable;

/**
 * 允许自定义修改新的bean实例 工厂钩子 BeanPostProcessor是在spring容器加载了bean的定义文件，并且实例化bean之后执行的。
 * <p>Factory hook that allows for custom modification of new bean instances,
 * e.g. checking for marker interfaces or wrapping them with proxies.
 *
 * <p>ApplicationContexts can autodetect BeanPostProcessor beans in their
 * bean definitions and apply them to any beans subsequently created. Plain bean factories allow for
 * programmatic registration of post-processors, applying to all beans created through this
 * factory.
 *
 * <p>Typically, post-processors that populate beans via marker interfaces
 * or the like will implement {@link #postProcessBeforeInitialization}, while post-processors that
 * wrap beans with proxies will normally implement {@link #postProcessAfterInitialization}.
 *
 * @author Juergen Hoeller
 * @see InstantiationAwareBeanPostProcessor
 * @see DestructionAwareBeanPostProcessor
 * @see ConfigurableBeanFactory#addBeanPostProcessor
 * @see BeanFactoryPostProcessor
 * @since 10.10.2003
 */
/*
	BeanPostProcessor是在spring容器加载了bean的定义文件，并且实例化bean之后执行的。
	BeanPostProcessor的执行顺序是在BeanFactoryPostProcessor之后。
	这个接口只有两个方法，他允许自定义的去修改spring给我们创建的类。
	在Bean初始化时处理  有before after
	AOP就是在这个阶段去实现的

	spring中，有内置的一些BeanPostProcessor实现类，例如：
	支持@Resource注解的注入
	org.springframework.context.annotation.CommonAnnotationBeanPostProcessor：
	支持@Required注解的注入
	org.springframework.beans.factory.annotation.RequiredAnnotationBeanPostProcessor：
	支持@Autowired注解的注入
	org.springframework.beans.factory.annotation.AutowiredAnnotationBeanPostProcessor：
	支持@PersistenceUnit和@PersistenceContext注解的注入
	org.springframework.orm.jpa.support.PersistenceAnnotationBeanPostProcessor：
	用来为bean注入ApplicationContext等容器对象
	org.springframework.context.support.ApplicationContextAwareProcessor：

 */
public interface BeanPostProcessor {

	/**
	 * Apply this BeanPostProcessor to the given new bean instance <i>before</i> any bean
	 * initialization callbacks (like InitializingBean's {@code afterPropertiesSet} or a custom
	 * init-method). The bean will already be populated with property values. The returned bean
	 * instance may be a wrapper around the original.
	 * <p>The default implementation returns the given {@code bean} as-is.
	 *
	 * @param bean     the new bean instance
	 * @param beanName the name of the bean
	 * @return the bean instance to use, either the original or a wrapped one; if {@code null}, no
	 * subsequent BeanPostProcessors will be invoked
	 * @throws org.springframework.beans.BeansException in case of errors
	 * @see org.springframework.beans.factory.InitializingBean#afterPropertiesSet
	 */
	@Nullable
	default Object postProcessBeforeInitialization(Object bean, String beanName)
			throws BeansException {
		return bean;
	}

	/**
	 * Apply this BeanPostProcessor to the given new bean instance <i>after</i> any bean
	 * initialization callbacks (like InitializingBean's {@code afterPropertiesSet} or a custom
	 * init-method). The bean will already be populated with property values. The returned bean
	 * instance may be a wrapper around the original.
	 * <p>In case of a FactoryBean, this callback will be invoked for both the FactoryBean
	 * instance and the objects created by the FactoryBean (as of Spring 2.0). The post-processor
	 * can decide whether to apply to either the FactoryBean or created objects or both through
	 * corresponding {@code bean instanceof FactoryBean} checks.
	 * <p>This callback will also be invoked after a short-circuiting triggered by a
	 * {@link InstantiationAwareBeanPostProcessor#postProcessBeforeInstantiation} method, in
	 * contrast to all other BeanPostProcessor callbacks.
	 * <p>The default implementation returns the given {@code bean} as-is.
	 *
	 * @param bean     the new bean instance
	 * @param beanName the name of the bean
	 * @return the bean instance to use, either the original or a wrapped one; if {@code null}, no
	 * subsequent BeanPostProcessors will be invoked
	 * @throws org.springframework.beans.BeansException in case of errors
	 * @see org.springframework.beans.factory.InitializingBean#afterPropertiesSet
	 * @see org.springframework.beans.factory.FactoryBean
	 */
	/*
		Spring AOP动态代理就是在BeanPostProcessor的PostProcessorAfterInitialization之后处理
		与Spring IoC解耦
	 */
	@Nullable
	default Object postProcessAfterInitialization(Object bean, String beanName)
			throws BeansException {
		return bean;
	}

}
